CREATE VIEW v_shipments AS
SELECT
  s.id,
  s.delivery_date,
  s.status,
  s.vendor_id,
  v.name  AS vendor_name,
  s.destination_id,
  COALESCE(s.destination_name, st.name) AS destination_name,
  s.created_at,
  s.updated_at
FROM shipments s
LEFT JOIN vendors v ON v.id = s.vendor_id
LEFT JOIN stores  st ON st.id = s.destination_id;
CREATE VIEW v_shipment_lines AS
SELECT
  sl.*,
  i.name AS item_name
FROM shipment_lines sl
LEFT JOIN items i ON i.id = sl.item_id;
CREATE TRIGGER trg_items_id_len
BEFORE INSERT ON items
FOR EACH ROW
WHEN length(NEW.id) <> 6 OR NEW.id GLOB '*[^0-9]*'
BEGIN
  SELECT RAISE(ABORT, 'items.id must be 6-digit numeric');
END;
CREATE TRIGGER trg_vendors_id_len
BEFORE INSERT ON vendors
FOR EACH ROW
WHEN length(NEW.id) <> 6 OR NEW.id GLOB '*[^0-9]*'
BEGIN
  SELECT RAISE(ABORT, 'vendors.id must be 6-digit numeric');
END;
CREATE TRIGGER trg_stores_id_len
BEFORE INSERT ON stores
FOR EACH ROW
WHEN length(NEW.id) <> 4 OR NEW.id GLOB '*[^0-9]*'
BEGIN
  SELECT RAISE(ABORT, 'stores.id must be 4-digit numeric');
END;
CREATE TRIGGER trg_shipment_lines_amount_ai
AFTER INSERT ON shipment_lines
BEGIN
  UPDATE shipment_lines
     SET amount = COALESCE(ship_qty,0) * COALESCE(unit_price,0)
   WHERE id = NEW.id;
END;
CREATE TRIGGER trg_shipment_lines_amount_au
AFTER UPDATE OF ship_qty, unit_price ON shipment_lines
BEGIN
  UPDATE shipment_lines
     SET amount = COALESCE(ship_qty,0) * COALESCE(unit_price,0)
   WHERE id = NEW.id;
END;
